    private void initMyComponents() {
        for (int l = 0; l < gc.CellsPerRow(); l++) {
            final int fi = l;
            for (int m = 0; m < gc.CellsPerRow(); m++) {
                final int fj = m;
//                Cell cell = cells[i][j];
                gc.addPropertyChangeListner(new PropertyChangeListener() {
                    @Override
                    public void propertyChange(PropertyChangeEvent evt) {
                        Cell.State state = (Cell.State) evt.getNewValue();
                        Cell thecell = (Cell) evt.getOldValue();
                        boolean hasPipe = state == Cell.State.CROSS_ENTERD
                                || state == Cell.State.ENTERD
                                || state == Cell.State.CROSS_LEAVED
                                || state == Cell.State.LEAVED;
                        Color color = null;
                        switch (evt.getPropertyName().toUpperCase()) {
                            case "UP":
                                if (hasPipe) {
                                    color = thecell.getColor(Cell.Side.UP);
                                }
                                mybuttons[fi][fj].setDrawUp(hasPipe, color);
                                break;
                            case "DOWN":
                                if (hasPipe) {
                                    color = thecell.getColor(Cell.Side.DOWN);
                                }
                                mybuttons[fi][fj].setDrawDown(hasPipe, color);
                                break;
                            case "RIGHT":
                                if (hasPipe) {
                                    color = thecell.getColor(Cell.Side.RIGHT);
                                }
                                mybuttons[fi][fj].setDrawRight(hasPipe, color);
                                break;
                            case "LEFT":
                                if (hasPipe) {
                                    color = thecell.getColor(Cell.Side.LEFT);
                                }
                                mybuttons[fi][fj].setDrawLeft(hasPipe, color);
                                break;
                        }
                    }
                }, l, m);
            }
        }
        gc.addPropertyChangeListner(new PropertyChangeListener() {

            @Override
            public void propertyChange(PropertyChangeEvent evt) {
                if (evt.getPropertyName().equalsIgnoreCase("unfilled")) {
                    changeSupport.firePropertyChange("finished", false, true);
                } else if (evt.getPropertyName().equalsIgnoreCase("maze")) {
                    int length = ((Maze) evt.getNewValue()).getLength();
                    for (int k = 0; k < length; k++) {
                        for (int l = 0; l < length; l++) {
                            Cell theCell = ((Maze) evt.getNewValue()).getCellAt(k, l);

                            boolean up = false, down = false, left = false, right = false;
                            Cell.Side side = theCell.getEntered(true);
                            if (side != null) {
                                switch (side) {
                                    case RIGHT:
                                        right = true;
                                        break;
                                    case LEFT:
                                        left = true;
                                        break;
                                }
                            }
                            side = theCell.getLeaved(true);
                            if (side != null) {
                                switch (side) {
                                    case RIGHT:
                                        right = true;
                                        break;
                                    case LEFT:
                                        left = true;
                                        break;
                                }
                            }
                            side = theCell.getEntered(false);
                            if (side != null) {
                                switch (side) {
                                    case UP:
                                        up = true;
                                        break;
                                    case DOWN:
                                        down = true;
                                        break;
                                }
                            }
                            side = theCell.getLeaved(false);
                            if (side != null) {
                                switch (side) {
                                    case UP:
                                        up = true;
                                        break;
                                    case DOWN:
                                        down = true;
                                        break;
                                }
                            }
                            mybuttons[k][l].setDrawUp(up, theCell.getColor(Cell.Side.UP));
                            mybuttons[k][l].setDrawDown(down, theCell.getColor(Cell.Side.DOWN));
                            mybuttons[k][l].setDrawRight(right, theCell.getColor(Cell.Side.RIGHT));
                            mybuttons[k][l].setDrawLeft(left, theCell.getColor(Cell.Side.LEFT));

                        }
                    }
                }
            }
        });

    }

